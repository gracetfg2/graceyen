            <b>To Stop or Not to Stop? A Case Study of an Early  intervention for Data Collection </b>       
            <p id="project-content">Deciding the sample size of a qualitative survey is a critical issue. Insufficient feedback might lose possible insights while too much feedback results in a waste of time and money. We addressed this problem by calculating the information saturation level and visualizing the evolution of main topics derived from feedback pool. We built an easy-to-use Web tool allowing Kickstarter project creators to collect feedback from Amazon Mechanical Turk. An interactive visualization was then presented to the creators to help them better understand the current dataset. 
