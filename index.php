        <div class="col-xs-8">     
            <b>Brainstagram: Presenting Diverse  Visually Inspiring Stimuli for Creative  Design </b>       
            <p id="project-content">We built a data-driven brainstorming tool leveraging Instagram hashtags to assist designers in finding inspiration. One challenge designers face when brainstorming is knowing how to search for unlikely sources of inspiration. We present Brainstagram, which organizes visual stimuli through tag co-occurrence to display related concepts. This allows users to easily navigate from relevant to unexpected images. Our pilot study demonstrates that Brainstagram as a potentially effective tool in the brainstorming process. 
            </p>

