        <p id='bio-sketch'>   
        Hi I am Yu-Chun, and I am a fourth-year CS PhD student studying Human-Computer Interaction at UIUC, working with <a href="http://orchid.cs.illinois.edu/people/index.html" target="_blank">Professor Brian Bailey</a> on the topic of crowdsourcing and creative design process. My research focuses on how to connect designers with limited sources to the potential stakehoders throughout the design process. Within the scope, I built a plaform, CRAFT, that enables novice designers to gather feedback from multiple crowd genres including social networks, online communities, and task markets, and at different design stages. The platform has been used by 200+ designers for collecting feedback from 2000+ feedback providers, and has been deployed in the User-Interface Design class at UIUC for connecting students with online crowds . My current research addressing another challenging problem of how to help designers interpret
feedback more deeply when that feedback is gathered outside the structured environment of aclassroom or work setting. 
    </p>

